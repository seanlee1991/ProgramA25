public class Maths {

    public static void main(String[] args) {
        int num1 = 233, num2 = 11;
        double sum, diff, prod, quot;

        sum = num1 + num2;
        
        diff = num1 - num2;

        prod = num1 * num2;

        quot = num1 / num2;
        
        
        System.out.println(sum);
        System.out.println(diff);
        System.out.println(prod);
        System.out.println(quot);
    }
}